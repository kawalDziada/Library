package com.example.library.user;

import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.NoArgsConstructor;

import java.util.UUID;

@Entity
@Table(name = "users")
@Getter
@AllArgsConstructor
@NoArgsConstructor
class User {

    @Id
    @GeneratedValue
    private UUID id;
    private String name;

    static User of(String name) { // id will be generated by database, so you would need a way to create object without if,
        // static creator methods are a good way for this, but you could also define a constructor with just name
        // for Book, you should be able to create it from NewBookDto, so you can have of method with exactly this - NewBookDto as an argument
        return new User(null, name);
    }
}
